    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (evt.getClickCount() == 2) {
            String TeacherName = String.valueOf(jTable1.getValueAt(row, 1));
            en.getTeacherName().setText(TeacherName);

            String sub = String.valueOf(jTable1.getValueAt(row, 3));
            en.getSubject().setText(sub);

            int teacherid = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            en.setTeacherID(teacherid);

            int subjectid = Integer.parseInt(jTable1.getValueAt(row, 2).toString());
            en.setSubjectID(subjectid);

            this.dispose();

        }
    }//GEN-LAST:event_jTable1MouseClicked

